export type AdminI18nKey = keyof (typeof dictionaries)["fr"];

export function normalizeAdminLang(value: string | null | undefined): AdminLang {
  return value === "en" ? "en" : "fr";
}

export function t(lang: AdminLang, key: AdminI18nKey, params?: Record<string, string | number>): string {
  const template = dictionaries[lang][key] ?? dictionaries.fr[key];
  if (!params) return template;

  return template.replace(/\{(\w+)\}/g, (_, token: string) => String(params[token] ?? ""));
}
